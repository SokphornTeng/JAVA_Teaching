package JAVA_Teaching.Demo_Java_Teaching.JAVA_BASIC;

public class Relational_Operator {

	public static void main(String[] args) {
	     
	    //Relational Operator = Comparison Operator
	      //   ==, !=, >, <, >=, <=     we use it for condition of loop, if ….
	        int a = 10;
	        int b = 20;
	        System.out.print(a==b);
	        System.out.print(a!=b);
	        System.out.print(a>b);
	        System.out.print(a<b);
	        System.out.print(a>=b);
	        System.out.print(a<=b);
	        
	        
	      //  Logical Operator -> តកកាកវិជ្ជា
	        //&&, , ! (not)  -> use with condition
	        System.out.print((20>10) && (25>15) );// -> true it must true
	        System.out.print((20>10) || (2>15) ); //-> true
	        System.out.print(!(20>10) );// -> false
	  
	  }
	
}
