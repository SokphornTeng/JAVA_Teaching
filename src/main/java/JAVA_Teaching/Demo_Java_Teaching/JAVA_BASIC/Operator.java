package JAVA_Teaching.Demo_Java_Teaching.JAVA_BASIC;

public class Operator {

	public static void main(String[] args) {
	     
	    // Arithmetic Operator + - / * %
	    int a = 2;
	    int b = 4;
	    System.out.print(a + b);
	    System.out.print(a - b);
	    System.out.print(a / b);
	    System.out.print(a * b);
	    
	    // Increment and Decrement Operator -- ++
	    int i = 30;
	    i--; // 29
	    i++; // 31
	    System.out.print(i);
	    
	    // Assignment Operator (+ - / * %) + =
	    int j = 20;
	    j += 4;
	    j -= 3;
	    j /= 5;
	    j *= 2;
	    System.out.print(j);
	  }
	
}
